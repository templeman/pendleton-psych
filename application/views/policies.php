<h2>policies and procedures</h2>
<p>The staff at Psychological Services of Pendleton, LLC utilize a
variety of forms and questionnaires. As a courtesy to our clients, we
have made some of these forms available on this site. Documents are
listed by clinician, and may be viewed on your web browser, printed, or
downloaded and saved to your computer. Forms are provided in PDF format.
If you do not already have a program capable of reading PDF files, you
may <a href="http://www.adobe.com/products/acrobat/readstep2.html">download the free Adobe&reg; Reader&reg; here</a>.</p>
